    public boolean CekInternal(JInternalFrame form) {

        JInternalFrame frm[] = Deks.getAllFrames();

        boolean aktif = false;
        for (int i = 0; i < frm.length; i++) {
            if (frm[i].equals(form)) {
                aktif = true;
            }
        }
        return aktif;
    }

    public static void tengah(JInternalFrame form) {
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        int x = (dim.width - form.getSize().width) / 2;
        int y = (dim.height - form.getSize().height) / 2;
        form.setLocation(x, y);
    }
    private void akses() {
        try {
            koneksidb konek = new koneksidb();
            PreparedStatement pst = konek.getCon().prepareStatement(
                    "select Hak_Akses from pegawai where User=?");
            
            pst.setString(1, log.getUser());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) { //ambil record selanjutnya
                if (rs.getString(1).equalsIgnoreCase("Admin")) {//sama dengan admin=> beda besar kecil berpengaruh
                    MiPetugas.setEnabled(true);
                    MiMenu.setEnabled(true);
                    MiPenjualan.setEnabled(true);
                    MiPembelian.setEnabled(true);
                    LapPenjualan.setEnabled(true);
                    LapPembelian.setEnabled(true);
                    LapMenu.setEnabled(true);
                } else if (rs.getString(1).equalsIgnoreCase("Operator")){
                    MiPetugas.setEnabled(false); // petugas
                    MiMenu.setEnabled(false); // daftar menu
                    MiPenjualan.setEnabled(true);
                    MiPembelian.setEnabled(true);
                    LapPenjualan.setEnabled(false);
                    LapPembelian.setEnabled(false);
                    LapMenu.setEnabled(false);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


