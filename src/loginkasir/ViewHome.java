    private void LapMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LapMenuActionPerformed

        try {
            koneksidb kon = new koneksidb();

            File file = new File("./src/laporan/LapMenu.jrxml");
            jasdes = JRXmlLoader.load(file);
            param.clear();
            jaspor = JasperCompileManager.compileReport(jasdes);
            jasprint = JasperFillManager.fillReport(jaspor, param, kon.getCon());
            JasperViewer.viewReport(jasprint, false);
            setVisible(true);
            setSize(840, 450);
        } catch (Exception ex) {
            Logger.getLogger(ViewHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_LapMenuActionPerformed

