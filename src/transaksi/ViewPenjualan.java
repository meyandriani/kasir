    private void TJmlKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TJmlKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if ("".equals(TSubTot.getText())) {
                transaksi tran = new transaksi();
                int Jml = Integer.parseInt(TJml.getText());
                int harga = Integer.parseInt(THarga.getText());
                int sub = 0;
                tran.setHarga(harga);
                tran.setJumlah(Jml);
                tran.hitung(Jml, harga, sub);

                TSubTot.setText(String.valueOf(tran.hitung(Jml, harga, sub)));
            } else {
                jual();
            }
        }
    }//GEN-LAST:event_TJmlKeyPressed

