    private void TBayarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TBayarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int totall = Integer.parseInt(TTOTAL.getText());
            int tbayar = Integer.parseInt(TBayar.getText());
            if (tbayar >= totall) {
                int kembali = tbayar - totall;
                TKembali.setText(String.valueOf(kembali));
            } else {
                int validasi = JOptionPane.showConfirmDialog(null, 
                        "MAAF, Uang Bayar Kurang !!", "Pemberitahuan", JOptionPane.OK_CANCEL_OPTION);
                if (validasi == JOptionPane.OK_OPTION) {
                    TBayar.setText("");
                    TKembali.setText("0");
                    TBayar.requestFocus();
                } else {
                    TBayar.setText("");
                    TKembali.setText("0");
                    TBayar.requestFocus();
                }
            }
            simpan();
        }
    }//GEN-LAST:event_TBayarKeyPressed

    private void simpan() {
        transaksi tran = new transaksi();
        int nota = Integer.parseInt(TNo.getText());
        int idPegawai = Integer.parseInt(TKasir.getText());
        String tanggal = TTgl.getText();
        String waktu = TWaktu.getText();
        String totaljual = TTOTAL.getText();
        int bayar = Integer.parseInt(TBayar.getText());
        int kembalian = Integer.parseInt(TKembali.getText());
        tran.setIdpegawai(idPegawai);
        tran.setTgl(tanggal);
        tran.setWaktu(waktu);
        tran.setTotal(total);
        tran.setBayar(bayar);
        tran.setKembalian(kembalian);
        inpenjualan.simpan(tran);
        for (int row = 0; row < TJual.getRowCount(); row++) {

            try {
                PreparedStatement st = kon.getCon().prepareStatement("insert into ayemtentrem.detail_jual "
                        + "(Id_Menu, Harga, Jumlah, Sub_Total ,Nota_Jual) values("
                        + "?,?,?,?,?)");

                st.setString(1, TJual.getValueAt(row, 0).toString());
                st.setString(2, TJual.getValueAt(row, 2).toString());
                st.setString(3, TJual.getValueAt(row, 3).toString());
                st.setString(4, TJual.getValueAt(row, 4).toString());
                st.setString(5, TNo.getText());
                st.execute();
            } catch (SQLException ex) {
                Logger.getLogger(ViewPenjualan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        cetaknota();
        bersih();
        no();
    }
