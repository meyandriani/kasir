    private void TBayarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TBayarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int totall = Integer.parseInt(TTOTAL.getText());
            int tbayar = Integer.parseInt(TBayar.getText());
            String ket = TKet.getText();
            if (tbayar >= totall) {
                
                simpan();
            } else {
                int validasi = JOptionPane.showConfirmDialog(null, 
                        "MAAF, Uang Bayar Kurang !!", "Pemberitahuan", JOptionPane.OK_CANCEL_OPTION);
                TKet.setText(ket + " Kurang Bayar = " + (totall - tbayar));

            }
        }
    }//GEN-LAST:event_TBayarKeyPressed

