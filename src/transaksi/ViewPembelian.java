    private void simpan(){
        transaksi tran = new transaksi();
        int nota = Integer.parseInt(TNo.getText());
        int idPegawai = Integer.parseInt(Tpegawai.getText());
        String tanggal = TTgl.getText();
        String waktu = TWaktu.getText();
        String totalbeli = TTOTAL.getText();
        int bayar = Integer.parseInt(TBayar.getText());
        String keterangan = TKet.getText();
        String Jenis = "";
        if (RBKredit.isSelected()) {
            Jenis = "Kredit";
        } else if (RBTunai.isSelected()) {
            Jenis = "Tunai";
        } else {
            JOptionPane.showConfirmDialog(null, 
                    "MAAF, Uang Pilih Jenis Pembayaran Dulu !!",
                    "Pemberitahuan", JOptionPane.OK_CANCEL_OPTION);
        }
        tran.setIdpegawai(idPegawai);
        tran.setTgl(tanggal);
        tran.setWaktu(waktu);
        tran.setTotal(total);
        tran.setBayar(bayar);
        tran.setJenis(Jenis);
        tran.setKeterangan(keterangan);
        inpembelian.simpan(tran);
        for (int row = 0; row < TabelBlanja.getRowCount(); row++) {

            try {
                PreparedStatement st = kon.getCon().prepareStatement(""
                        + "insert into ayemtentrem.detail_beli "
                        + "(Barang, Jumlah, Harga, Sub_Total ,Nota_Beli) values("
                        + "?,?,?,?,?)");

                st.setString(1, TabelBlanja.getValueAt(row, 0).toString());
                st.setString(2, TabelBlanja.getValueAt(row, 1).toString());
                st.setString(3, TabelBlanja.getValueAt(row, 2).toString());
                st.setString(4, TabelBlanja.getValueAt(row, 3).toString());
                st.setString(5, TNo.getText());
                st.execute();
            } catch (SQLException ex) {
                Logger.getLogger(ViewPembelian.class.getName()).log(Level.SEVERE, null, ex);
            }
        }       
        bersih();
        no();
    }
