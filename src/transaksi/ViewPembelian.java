    protected void beli() {

        int Jml = Integer.parseInt(TJml.getText());
        int Harga = Integer.parseInt(THarga.getText());
        int Sub = Integer.valueOf(TSubTot.getText());
        String Barang = TBarang.getText();

        total = (int) (total + Sub);
        TTOTAL.setText(String.valueOf(total));

        Object[] data = {Barang, Harga, Jml, Sub};

        tabelmodelbeli.addRow(data);
        TBarang.setText(null);
        THarga.setText(null);
        TJml.setText(null);
        TSubTot.setText(null);
        BSimpan.setEnabled(true);
    }
    private void THargaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_THargaKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if ("".equals(TSubTot.getText())) {
                transaksi tran = new transaksi();
                int Jml = Integer.parseInt(TJml.getText());
                int harga = Integer.parseInt(THarga.getText());
                int sub = 0;
                tran.setHarga(harga);
                tran.setJumlah(Jml);
                tran.hitung(Jml, harga, sub);

                TSubTot.setText(String.valueOf(tran.hitung(Jml, harga, sub)));
            } else {
                beli();
                TBarang.requestFocusInWindow();
            }

        }
    }//GEN-LAST:event_THargaKeyPressed

