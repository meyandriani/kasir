    /**
     * Digunakan untuk mengambl nomer pembelian sekarang
     */
    public final void no() {

        try {

            koneksidb konek = new koneksidb();
            PreparedStatement pst = konek.getCon().prepareStatement(""
                    + "select notabeli as nt  from pembelian order by notabeli DESC limit 1");
            rs = pst.executeQuery();
            if (rs.next()) {
                int nota = Integer.parseInt(rs.getString("nt")) + 1;
                TNo.setText(Integer.toString(nota));
            } else {
                int nota = 1;
                TNo.setText(Integer.toString(nota));
            }
            rs.close();
        } catch (SQLException e) {
        }
        TJml.requestFocusInWindow();
    }

