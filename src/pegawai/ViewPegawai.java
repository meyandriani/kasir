    private void tampilpegawai() {
        listpegawai = inpegawai.tampilpegawai();
        tabelmodel = (DefaultTableModel) TDaftarP.getModel();
        tabelmodel.setRowCount(0);
        
        for (pegawai dpegawai : listpegawai) {
            tabelmodel.addRow(new Object[]{dpegawai.getIdpegawai(),
                dpegawai.getUser(), dpegawai.getNama(),
                dpegawai.getAkses(), dpegawai.getNohp(), dpegawai.getAlamat()});
            
        }
        int ambilbaris = TDaftarP.getRowCount();
        if (ambilbaris > 0) {
            TDaftarP.addRowSelectionInterval(0, 0);
        }
    }
    
