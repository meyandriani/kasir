    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        pegawai pgw = new pegawai();
        String nama = TNama.getText();
        String no = TNo.getText();
        String alamat = TAlamat.getText();
        String akses = CBAkses.getSelectedItem().toString();
        Integer passwd = TPass.getText().hashCode();
        String user = TUser.getText();
        pgw.setUser(user);
        pgw.setAkses(akses);
        pgw.setAlamat(alamat);
        pgw.setNama(nama);
        pgw.setNohp(no);
        pgw.setPassword(passwd.toString());
        //elum ada set password sayang
        if (updSQLFlag == false) {
            if (nama.equals("") && no.equals("") && alamat.equals("") 
                    && akses.equals("---Pilih Hak Akses---")) {
                JOptionPane.showMessageDialog(null, "Data Harus Diisi Lengkap !",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
                TNama.grabFocus();
            } else {
                inpegawai.simpan(pgw);
            }            
            tampilpegawai();
        }
        bersih();
        pasif();
        BSimpan.setEnabled(false);
    }//GEN-LAST:event_BSimpanActionPerformed
    
    final public void tampiltext() {
        baris = TDaftarP.getSelectedRow();
        if (baris >= 0) {
            Tid.setText(tabelmodel.getValueAt(baris, 0).toString());
            TUser.setText(tabelmodel.getValueAt(baris, 1).toString());
            TNama.setText(tabelmodel.getValueAt(baris, 2).toString());
            CBAkses.setSelectedItem(tabelmodel.getValueAt(baris, 3));
            TNo.setText(tabelmodel.getValueAt(baris, 4).toString());
            TAlamat.setText(tabelmodel.getValueAt(baris, 5).toString());
        }
    }
