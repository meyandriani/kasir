    private int idpegawai;
    private String user;
    private String nama;
    private String nohp;
    private String akses;
    private String password;
    private String alamat;

    /**
     * @return the idpegawai
     */
    public int getIdpegawai() {
        return idpegawai;
    }

    /**
     * @param idpegawai the idpegawai to set
     */
